#include <iostream>
#include <bits/stdc++.h>
using namespace std;

int main()
{
    long long num, cnt, N;
    cin >> num;
    N=0;
    cnt=0;
    for (int i = 1; N <= num; i++)
    {
       N=N+pow(i, 2);
       cnt++; 
    }

    cnt--;
    cout << cnt;
    return 0;
}